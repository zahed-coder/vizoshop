// Create the LanguageContext
export const LanguageContext = createContext();

// Create the LanguageProvider component
export const LanguageProvider = ({ children }) => {
  // Get preferred language from localStorage or default to 'en'
  const [lang, setLang] = useState(() => {
    const storedLang = localStorage.getItem('zshop_lang');
    return storedLang && translations[storedLang] ? storedLang : 'en';
  });

  // Effect to update document direction (RTL/LTR) and save preference
  useEffect(() => {
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang; // Set HTML lang attribute
    localStorage.setItem('zshop_lang', lang);
  }, [lang]);

  // Memoize the 't' (translate) function and the 'currentLang' object
  // This prevents unnecessary re-renders for consumers of the context.
  const contextValue = useMemo(() => {
    // 't' function: takes a key and returns the translated string
    const t = (key) => translations[lang][key] || key; // Fallback to key if translation missing
    return { lang, setLang, t };
  }, [lang]); // Recalculate if 'lang' changes

  return (
    <LanguageContext.Provider value={contextValue}>
      {children}
    </LanguageContext.Provider>
  );
};

// Custom hook to use the language context
export const useLanguage = () => useContext(LanguageContext);